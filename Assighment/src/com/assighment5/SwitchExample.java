package com.assighment5;

public class SwitchExample {
	public static void main(String[] args) {
		String month = "march";

		switch (month) {
		case "january":
			System.out.println("this is january");
			break;
		case "february":
			System.out.println("this is february");
			break;
		case "march":
			System.out.println("this is march");
			break;
		case "april":
			System.out.println("this is april");
			break;
		case "may":
			System.out.println("this is may");
			break;
		case "june":
			System.out.println("this is june");
			break;
		case "july":
			System.out.println("this is july");
			break;
		case "august":
			System.out.println("this is august");
			break;
		case "september":
			System.out.println("this is september");
			break;
		case "octomber":
			System.out.println("this is octomber");
			break;
		case "november":
			System.out.println("this is november");
			break;
		case "december":
			System.out.println("this is december");
			break;
			default:
				System.out.println("invailed choice");

		}
	}
}
