package Assighment4;

public class Swap {
	public static void main(String[] args) {
		int a = 10;
		int b = 20;
		int temp;
		System.out.println("before swaping");
		System.out.println(a);
		System.out.println(b);
		System.out.println("after the swaping");
		temp = a;
		a = b;
		b = temp;
		
		System.out.println(a);
		System.out.println(b);

	}
}
