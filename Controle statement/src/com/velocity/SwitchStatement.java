package com.velocity;

public class SwitchStatement {
public static void main(String[] args) {
	int no=2;
	switch(no) {
	case 1:
		System.out.println("this is 1 num");
		break;
	case 2:
		System.out.println("this is 2 num");
		break;
	case 3:
		System.out.println("this is 3 num");
		break;
	case 4:
		System.out.println("this is 4 num");
		break;
	}
}
}
